                    <dd align="justify">Web-based portal for All India Council for Technical Education (AICTE), where information about all academic activities across the country is available in chronological order and in subject areas so as to enable the students, faculty, and researchers to access information and prepare well in advance and participate effectively and successfully.</dd>
                    <br>
                    <dt><strong>Project summary:</strong></dt>
                    <dd align="justify">Our web-portal allows institutes to briefly list their upcoming academic and cultural events that are open for all. These listed events are available for the students on a single unified web-portal, making it very convenient for the interested participants.<br>Our project can be viewed as a way of encouraging innovation in the students by making the hunt for a necessary platform easier.
                    </dd><br>
                    <dt><strong>Background:</strong></dt>
                    <dd align="justify">AICTE is a statutory body, and a national-level council for technical education, under the Department of Higher Education. This document describes the scope, objectives and goal of the new system. In addition to describing non-functional requirements, this document models the functional requirements with use cases, interaction diagrams, and class models.
                        <br>Teachers, students are encouraged to take up research and innovate. Current accreditation, ranking and grading norms emphasises on research work and publications by both students and faculty. In order to take up research work and subsequently make publications of research findings, to collaborate and network with others who are working on similar problems there is a need to participate in global, national and local workshops, seminars, symposia, conferences, etc. Students have to search for information about dates, places and themes of such academic confluences. Lot of time and efforts are wasted in this regard and despite that they may miss out vital information and chances to make presentations. Availability of a single portal will be extremely useful for obtaining the information about all academic activities across the country, universities and international arena in chronological order and in subject areas so as to enable the students, faculty, and researchers to access information and prepare well in advance and participate effectively and successfully.
                    </dd><br>
                    <dt><strong>Project scope:</strong></dt>
                    <dd align="justify">The current version of the web-portal only facilitates event listing by the respective institute and requires a dedicated personnel to keep the portfolio updated.
                        <br>The future versions can include features like listing events by searching the web and not requiring updates from the institutes.
                        <br>Further, the coming versions can optimise the user preferences using acute A.I. and give accurate suggestions to the user based on their previous activity and numerous other factors.
